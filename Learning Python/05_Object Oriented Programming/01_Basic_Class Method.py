class Car:
    def __init__(self, make, model, year):
        self.make = make
        self.model = model
        self.year = year

my_car = Car("Ford", "Mustang", 2020)
print(my_car.make)
print(my_car.model)
print(my_car.year)

class Car1:
    def __init__(self, model, brand, year,):
        self.model = model
        self.brand = brand
        self.year = year

